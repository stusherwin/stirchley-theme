      <h1 class="">What is a <i>Co-operative</i>?</h1>
      <img src="<?= get_stylesheet_directory_uri() ?>/img/coop-marque2.png" class="" />
      <p>According to the <a href="https://www.ica.coop/en">International <i>Co-operative</i> Alliance</a>, a <i>co-operative</i> is <em>an autonomous association of persons united voluntarily to meet their  common economic, social, and cultural needs and aspirations through a jointly-owned and democratically controlled enterprise</em>.</p>
      <p>Cooperatives are based on the values of self-help, self-responsibility, democracy, equality, equity and solidarity. In the tradition of their founders, cooperative members believe in the ethical values of honesty, openness, social responsibility and caring for others. Co-operatives also follow the <a href="https://www.uk.coop/the-hive/is-a-co-op-right-for-you/getting-started/co-op-movement/values-principles">seven <i>Co-operative</i> Principles</a>.</p>
      <p>As we are a <i>co-operative</i>, each one of our tenants will have an equal say in the running of the building in which they live or work. 
      We are a non-profit organisation, so any profits will be reinvested as the members see fit. 
      We wish to support and grow other <i>co-operatives</i> in the community by providing them with secure and affordable premises.</p>

